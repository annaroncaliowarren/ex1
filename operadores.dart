void main() {

  double nota1 = 10;
  double nota2 = 9.5;

  // soma
  print(nota1 + nota2);

  // subtrair
  print(nota1 - nota2);

  // multiplicar
  print(nota1 * nota2);

  // divis천a
  print(nota1 / nota2);
  print(nota1 ~/ nota2); // retorna apenas a parte inteira da divis찾o
  print(nota1 % nota2); // resto da divis찾o

  // invers찾o
  // print(nota1 = -nota2);

  // maior / maior ou igual
  print(nota1 > nota2);
  print(nota1 >= nota2);

  // menor / menor ou igual
  print(nota1 < nota2);
  print(nota1 <= nota2);

  // igualdade / diferente
  print(nota1 == nota2);
  print(nota1 != nota2);
}